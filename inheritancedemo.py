class Employee:
    def setid(self,id):
        self.id=id

    def getid(self):
        return self.id

    def setName(self,name):
        self.name=name

    def getName(self):
        return self.name

    def setAddress(self,address):
        self.address=address

    def getAddress(self):
        return self.address

    def setSalary(self,salary):
        self.salary=salary

    def getSalary(self):
        return self.salary